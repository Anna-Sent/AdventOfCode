    private static int test(String s) {
        String[] instructions = s.split("\n");

        int max = 0;
        for (String instruction : instructions) {
            Matcher matcher = pattern.matcher(instruction);
            assert matcher.matches() : "not matches: " + instruction;
            assert matcher.groupCount() == 6 : "group count is " + matcher.groupCount();
            String arg1 = matcher.group(1);
            int arg1Value = getArgValue(arg1);
            String op1 = matcher.group(2);
            int num1 = Integer.parseInt(matcher.group(3));
            String arg2 = matcher.group(4);
            int arg2Value = getArgValue(arg2);
            String op2 = matcher.group(5);
            int num2 = Integer.parseInt(matcher.group(6));
            if (condition(arg2Value, num2, op2)) {
                int arg1NewValue = incOrDec(arg1Value, num1, op1);
                registers.put(arg1, arg1NewValue);
                max = Math.max(max, arg1NewValue);
            }
        }

        return max;
    }

    private static int incOrDec(int argValue, int num, String op) {
        switch (op) {
            case "inc":
                return argValue + num;
            case "dec":
                return argValue - num;
        }
        return argValue;
    }

    private static boolean condition(int argValue, int num, String op) {
        switch (op) {
            case "<":
                return argValue < num;
            case ">":
                return argValue > num;
            case "<=":
                return argValue <= num;
            case ">=":
                return argValue >= num;
            case "!=":
                return argValue != num;
            case "==":
                return argValue == num;
        }
        return false;
    }

    private static int getArgValue(String arg) {
        if (!registers.containsKey(arg)) {
            registers.put(arg, 0);
        }
        return registers.get(arg);
    }
