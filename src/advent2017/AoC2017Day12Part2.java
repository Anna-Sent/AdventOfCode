    public static int test(String s) {
        Map<Integer, Set<Integer>> programs = new HashMap<>();
        String[] parts = s.split("\n");
        for (String part : parts) {
            String[] parts1 = part.split(" <-> ");
            int programFrom = Integer.parseInt(parts1[0]);
            String[] parts2 = parts1[1].split(", ");
            for (String part2 : parts2) {
                int programTo = Integer.parseInt(part2);
                put(programs, programFrom, programTo);
                put(programs, programTo, programFrom);
            }
        }
        List<Set<Integer>> availabilityList = new ArrayList<>();
        for (Integer program : programs.keySet()) {
            boolean contains = false;
            for (Set availabilitySet : availabilityList) {
                if (availabilitySet.contains(program)) {
                    contains = true;
                    break;
                }
            }
            if (!contains) {
                Set<Integer> availabilitySet = new HashSet<>();
                count(programs, program, availabilitySet);
                availabilityList.add(availabilitySet);
            }
        }
        return availabilityList.size();
    }

    private static void count(Map<Integer, Set<Integer>> map, int from, Set<Integer> availability) {
        Set<Integer> children = map.get(from);
        if (children == null) {
            availability.add(from);
            return;
        }
        for (Integer to : children) {
            if (availability.contains(to)) {
                continue;
            }
            availability.add(to);
            count(map, to, availability);
        }
    }

    private static void put(Map<Integer, Set<Integer>> map, int from, int to) {
        Set<Integer> children = map.computeIfAbsent(from, key -> new HashSet<>());
        children.add(to);
    }
