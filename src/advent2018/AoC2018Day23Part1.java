    private static int test(String s) {
        String tokens[] = s.split("\n");
        List<Nanobot> nanobots = new ArrayList<>();
        Nanobot strongest = null;
        for (String token : tokens) {
            Matcher matcher = pattern.matcher(token);
            assert matcher.matches() : "not matches " + token;
            Nanobot nanobot = new Nanobot();
            nanobot.point.x = Integer.parseInt(matcher.group(1));
            nanobot.point.y = Integer.parseInt(matcher.group(2));
            nanobot.point.z = Integer.parseInt(matcher.group(3));
            nanobot.r = Integer.parseInt(matcher.group(4));
            nanobots.add(nanobot);
            if (strongest == null || strongest.r < nanobot.r) {
                strongest = nanobot;
            }
        }

        int count = 0;
        for (Nanobot nanobot : nanobots) {
            int distance = nanobot.point.manhattanDistance(strongest.point);
            if (distance <= strongest.r) {
                ++count;
            }
        }
        return count;
    }

    private static class Nanobot {
        Point3D point = new Point3D();
        int r;
    }
