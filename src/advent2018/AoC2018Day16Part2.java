    private static int test(String part1Input, String part2Input) {
        Pattern beforePattern = Pattern.compile("Before: \\[(\\d), (\\d), (\\d), (\\d)\\]");
        Pattern opPattern = Pattern.compile("(\\d+) (\\d) (\\d) (\\d)");
        Pattern afterPattern = Pattern.compile("After:\\s+\\[(\\d), (\\d), (\\d), (\\d)\\]");
        String[] tokens = part1Input.split("\n");
        List<String> filteredTokens = new ArrayList<>();
        for (String token : tokens) {
            token = token;
            if (!token.isEmpty()) {
                filteredTokens.add(token);
            }
        }
        int opcode, a, b, c;
        int[] registers1 = new int[4], registers2 = new int[4];
        Map<Integer, Set<String>> candidates = new HashMap<>();
        for (int i = 0; i <= filteredTokens.size() - 3; i += 3) {
            Matcher matcher;
            String before = filteredTokens.get(i);
            matcher = beforePattern.matcher(before);
            if (!matcher.matches()) {
                System.out.println("not matches before " + before);
            }
            for (int j = 0; j < 4; ++j) {
                registers1[j] = Integer.parseInt(matcher.group(j + 1));
            }
            String op = filteredTokens.get(i + 1);
            matcher = opPattern.matcher(op);
            if (!matcher.matches()) {
                System.out.println("not matches op " + op);
            }
            opcode = Integer.parseInt(matcher.group(1));
            a = Integer.parseInt(matcher.group(2));
            b = Integer.parseInt(matcher.group(3));
            c = Integer.parseInt(matcher.group(4));
            String after = filteredTokens.get(i + 2);
            matcher = afterPattern.matcher(after);
            if (!matcher.matches()) {
                System.out.println("not matches after " + after);
            }
            for (int j = 0; j < 4; ++j) {
                registers2[j] = Integer.parseInt(matcher.group(j + 1));
            }

            int changesCount = 0;
            for (int j = 0; j < 4; ++j) {
                boolean changed = registers1[j] != registers2[j];
                changesCount += changed ? 1 : 0;
            }
            if (changesCount != 1 && changesCount != 0) {
                System.out.println("expected one or zero changes");
            }

            Set<String> matchingCandidates = findMatchingCandidates(opcode, a, b, c, registers1, registers2);
            candidates.compute(opcode, (key, value) -> {
                if (value == null) {
                    value = new HashSet<>();
                    value.addAll(matchingCandidates);
                } else {
                    value.retainAll(matchingCandidates);
                }
                return value;
            });
        }

        while (!allDefined(candidates)) {
            for (Map.Entry<Integer, Set<String>> entry : candidates.entrySet()) {
                if (entry.getValue().size() == 1) {
                    for (Map.Entry<Integer, Set<String>> otherEntry : candidates.entrySet()) {
                        if (entry != otherEntry) {
                            otherEntry.getValue().removeAll(entry.getValue());
                        }
                    }
                }
            }
        }

        int[] registers = new int[4];
        tokens = part2Input.split("\n");
        for (String token : tokens) {
            Matcher matcher = opPattern.matcher(token);
            if (!matcher.matches()) {
                System.out.println("not matches op " + token);
            }
            opcode = Integer.parseInt(matcher.group(1));
            a = Integer.parseInt(matcher.group(2));
            b = Integer.parseInt(matcher.group(3));
            c = Integer.parseInt(matcher.group(4));
            String opname = candidates.get(opcode).toArray(new String[1])[0];
            switch (opname) {
                case "addr":
                    registers = addr(registers, a, b, c);
                    break;
                case "addi":
                    registers = addi(registers, a, b, c);
                    break;
                case "mulr":
                    registers = mulr(registers, a, b, c);
                    break;
                case "muli":
                    registers = muli(registers, a, b, c);
                    break;
                case "banr":
                    registers = banr(registers, a, b, c);
                    break;
                case "bani":
                    registers = bani(registers, a, b, c);
                    break;
                case "borr":
                    registers = borr(registers, a, b, c);
                    break;
                case "bori":
                    registers = bori(registers, a, b, c);
                    break;
                case "setr":
                    registers = setr(registers, a, b, c);
                    break;
                case "seti":
                    registers = seti(registers, a, b, c);
                    break;
                case "gtir":
                    registers = gtir(registers, a, b, c);
                    break;
                case "gtri":
                    registers = gtri(registers, a, b, c);
                    break;
                case "gtrr":
                    registers = gtrr(registers, a, b, c);
                    break;
                case "eqir":
                    registers = eqir(registers, a, b, c);
                    break;
                case "eqri":
                    registers = eqri(registers, a, b, c);
                    break;
                case "eqrr":
                    registers = eqrr(registers, a, b, c);
                    break;
            }
        }

        return registers[0];
    }

    private static boolean allDefined(Map<Integer, Set<String>> candidates) {
        for (Map.Entry<Integer, Set<String>> entry : candidates.entrySet()) {
            if (entry.getValue().size() != 1) {
                return false;
            }
        }
        return true;
    }

