    public static int test(String s) {
        Pattern pattern = Pattern.compile("(-?\\d+),(-?\\d+),(-?\\d+),(-?\\d+)");
        String[] tokens = s.split("\n");
        List<Point4D> points = new ArrayList<>();
        for (String token : tokens) {
            Matcher matcher = pattern.matcher(token);
            assert matcher.matches() : "invalid input " + token;
            Point4D point = new Point4D();
            point.x = Integer.parseInt(matcher.group(1));
            point.y = Integer.parseInt(matcher.group(2));
            point.z = Integer.parseInt(matcher.group(3));
            point.t = Integer.parseInt(matcher.group(4));
            points.add(point);
        }
        return numberOfConstellations(points);
    }

    private static int numberOfConstellations(List<Point4D> points) {
        List<Set<Point4D>> constellations = new ArrayList<>();
        for (Point4D point : points) {
            Set<Set<Point4D>> added = new HashSet<>();
            for (Set<Point4D> constellation : constellations) {
                boolean close = false;
                for (Point4D otherPoint : constellation) {
                    if (otherPoint.manhattanDistance(point) <= 3) {
                        close = true;
                        break;
                    }
                }
                if (close) {
                    constellation.add(point);
                    added.add(constellation);
                }
            }
            if (added.isEmpty()) {
                Set<Point4D> constellation = new HashSet<>();
                constellation.add(point);
                constellations.add(constellation);
            } else if (added.size() > 1) {
                constellations.removeAll(added);
                Set<Point4D> union = new HashSet<>();
                for (Set<Point4D> constellation : added) {
                    for (Point4D otherPoint : constellation) {
                        union.add(otherPoint);
                    }
                }
                constellations.add(union);
            }
        }
        return constellations.size();
    }
