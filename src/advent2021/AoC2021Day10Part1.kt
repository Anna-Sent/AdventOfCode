private fun test(input: String): Int {
    val lines = input.split("\n")
    return lines.map { score(it) }.sum()
}

private fun score(line: String): Int {
    var newLine = line
    var oldLine: String
    while (true) {
        oldLine = newLine
        newLine = newLine.replace("()", "")
                .replace("[]", "")
                .replace("{}", "")
                .replace("<>", "")
        if (oldLine == newLine) {
            for (c in oldLine) {
                when (c) {
                    ')' -> return 3
                    ']' -> return 57
                    '}' -> return 1197
                    '>' -> return 25137
                }
            }
            return 0
        }
    }
}
