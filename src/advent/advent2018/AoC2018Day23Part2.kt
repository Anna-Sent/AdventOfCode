private fun test(input: String): Int {
    val pattern = "pos=<(-?\\d+),(-?\\d+),(-?\\d+)>, r=(\\d+)".toPattern()
    val bots = input.split("\n").map {
        val matcher = pattern.matcher(it)
        assert(matcher.matches()) { "not matches $it" }
        val x = matcher.group(1).toInt()
        val y = matcher.group(2).toInt()
        val z = matcher.group(3).toInt()
        val point = Point3D(x, y, z)
        val r = matcher.group(4).toInt()
        Nanobot(point, r)
    }.toSet()

    val startPosition = Point3D(0, 0, 0)

    var currentRadius = max(bots.deltaBy { it.point.x }, max(bots.deltaBy { it.point.y }, bots.deltaBy { it.point.z }))

    var currentBots = setOf(Nanobot(Point3D(0, 0, 0), currentRadius))

    while (currentRadius > 0) {
        currentRadius = (currentRadius / 2) + if (currentRadius > 2) 1 else 0

        val newGeneration = currentBots.flatMap { bot ->
            bot.point.neighbors(currentRadius).map { currentPoint ->
                Nanobot(currentPoint, currentRadius).let { newBot ->
                    newBot to bots.count {
                        newBot.intersects(it)
                    }
                }
            }
        }

        val maxDistance = newGeneration.map { it.second }.max()!!

        currentBots = newGeneration.filter { it.second == maxDistance }.map { it.first }.toSet()
    }

    return currentBots.minBy { startPosition.manhattanDistance(it.point) }!!.point.manhattanDistance(startPosition)
}

private inline fun <T> Set<T>.deltaBy(block: (T) -> Int): Int {
    val values = map(block)
    return abs(values.max()!! - values.min()!!)
}

private fun Point3D.neighbors(delta: Int) = (-1..1).flatMap { xd ->
    (-1..1).flatMap { yd ->
        (-1..1).map { zd ->
            Point3D(x + xd * delta, y + yd * delta, z + zd * delta)
        }
    }
}

private data class Nanobot(val point: Point3D, val r: Int)

private fun Nanobot.intersects(other: Nanobot) = point.manhattanDistance(other.point) <= r + other.r
