private fun test(input: String): Int {

    var headPosition = Point(0, 0)
    var tailPositions = arrayListOf<Point>()
    for (i in 1..9) tailPositions += Point(0, 0)
    val set = mutableSetOf<Point>()
    set += tailPositions[8]
    for (line in input.split("\n")) {
        val (direction, stepsCountStr) = line.split(" ")
        val stepsCount = stepsCountStr.toInt()
        when (direction) {
            "U" -> {
                for (i in 1..stepsCount) {
                    headPosition = headPosition.up(1)
                    var head = headPosition
                    val newTailPositions = arrayListOf<Point>()
                    for (tailPosition in tailPositions) {
                        newTailPositions += catch(head, tailPosition).also { head = it }
                    }
                    tailPositions = newTailPositions
                    set += tailPositions[8]
                }
            }

            "D" -> {
                for (i in 1..stepsCount) {
                    headPosition = headPosition.down(1)
                    var head = headPosition
                    val newTailPositions = arrayListOf<Point>()
                    for (tailPosition in tailPositions) {
                        newTailPositions += catch(head, tailPosition).also { head = it }
                    }
                    tailPositions = newTailPositions
                    set += tailPositions[8]
                }
            }

            "R" -> {
                for (i in 1..stepsCount) {
                    headPosition = headPosition.right(1)
                    var head = headPosition
                    val newTailPositions = arrayListOf<Point>()
                    for (tailPosition in tailPositions) {
                        newTailPositions += catch(head, tailPosition).also { head = it }
                    }
                    tailPositions = newTailPositions
                    set += tailPositions[8]
                }
            }

            "L" -> {
                for (i in 1..stepsCount) {
                    headPosition = headPosition.left(1)
                    var head = headPosition
                    val newTailPositions = arrayListOf<Point>()
                    for (tailPosition in tailPositions) {
                        newTailPositions += catch(head, tailPosition).also { head = it }
                    }
                    tailPositions = newTailPositions
                    set += tailPositions[8]
                }
            }
        }
    }

    return set.size
}

private fun catch(headPosition: Point, tailPosition: Point): Point {
    return if (!tailPosition.isAdjacentTo(headPosition)) {
        var newTailPosition = tailPosition
        if (tailPosition.x != headPosition.x) {
            newTailPosition = newTailPosition.setX(headPosition)
        }
        if (tailPosition.y != headPosition.y) {
            newTailPosition = newTailPosition.setY(headPosition)
        }
        newTailPosition
    } else tailPosition
}

private fun Point.up(diff: Int): Point = Point(x, y + diff)
private fun Point.down(diff: Int): Point = Point(x, y - diff)
private fun Point.right(diff: Int): Point = Point(x + diff, y)
private fun Point.left(diff: Int): Point = Point(x - diff, y)
private fun Point.isAdjacentTo(point: Point): Boolean {
    return abs(point.x - x) == 1 && point.y == y || abs(point.y - y) == 1 && point.x == x || abs(point.x - x) == 1 && abs(point.y - y) == 1 || point == this
}

private fun Point.setX(point: Point): Point {
    return if (point.x > x) Point(x + 1, y) else Point(x - 1, y)
}

private fun Point.setY(point: Point): Point {
    return if (point.y > y) Point(x, y + 1) else Point(x, y - 1)
}
