private fun test(input: String): Int {
    var elves = mutableSetOf<Point>()
    var row = 1
    for (line in input.split("\n")) {
        var col = 1
        for (char in line) {
            if (char == '#') elves += Point(row, col)
            ++col
        }
        ++row
    }

    val directionsCount = Direction232.values().size
    var indexOfDirection = 0
    var round = 1
    var enoughSpace = false
    while (!enoughSpace) {
        enoughSpace = elves.all { elf ->
            elf.neighbors().all { neighbor -> neighbor !in elves }
        }
        if (!enoughSpace) {
            val newElves = mutableSetOf<Point>()

            val moves = mutableMapOf<Point, Point>()
            for (elf in elves) {
                val emptyAround = elf.neighbors().all { it !in elves }
                if (!emptyAround) {
                    for (k in indexOfDirection until indexOfDirection + directionsCount) {
                        val direction = Direction232.values()[k % directionsCount]
                        val neighbors = elf.neighbors(direction)
                        if (neighbors.all { it !in elves }) {
                            moves[elf] = elf.moveTo(direction)
                            break
                        }
                    }
                }
            }
            for (elf in elves) {
                val move = moves[elf]
                newElves += if (move == null) {
                    elf
                } else {
                    if (move !in moves.filterKeys { it != elf }.values) {
                        move
                    } else {
                        elf
                    }
                }
            }

            elves = newElves
            ++indexOfDirection
            ++round
        }
    }

    return round
}

private fun Point.neighbors() = mutableSetOf<Point>().apply {
    val set = setOf(1, 0, -1)
    for (i in set) {
        for (j in set) {
            if (i != 0 || j != 0) this += Point(x + i, y + j)
        }
    }
}

private fun Point.neighbors(direction: Direction232) = mutableSetOf<Point>().apply {
    when (direction) {
        Direction232.N -> {
            this += Point(x - 1, y)
            this += Point(x - 1, y - 1)
            this += Point(x - 1, y + 1)
        }

        Direction232.S -> {
            this += Point(x + 1, y)
            this += Point(x + 1, y - 1)
            this += Point(x + 1, y + 1)
        }

        Direction232.E -> {
            this += Point(x - 1, y + 1)
            this += Point(x, y + 1)
            this += Point(x + 1, y + 1)
        }

        Direction232.W -> {
            this += Point(x - 1, y - 1)
            this += Point(x, y - 1)
            this += Point(x + 1, y - 1)
        }
    }
}

private fun Point.moveTo(direction: Direction232) = when (direction) {
    Direction232.N -> Point(x - 1, y)
    Direction232.S -> Point(x + 1, y)
    Direction232.E -> Point(x, y + 1)
    Direction232.W -> Point(x, y - 1)
}

private enum class Direction232 {
    N, S, W, E
}
